def convert_map_input(input):
    return input.strip().split('\n')


test_map = convert_map_input(test_map_input)
my_map = convert_map_input(map_input)


def move_line(line, x):
    x %= len(line)
    new_line = (line + line)[x:len(line) + x]
    assert len(new_line) == len(line)
    return new_line


def move_map_right(my_map, x):
    return list(map(lambda line: move_line(line, x), my_map))


def move_map_down(my_map, y):
    return my_map[y:]


def move_map(my_map, x, y):
    return move_map_down(move_map_right(my_map, x), y)


def check_tree(my_map):
    return my_map[0][0] == '#'


def count_trees(my_map, x, y):
    tree_count = 0
    while my_map:
        if check_tree(my_map):
            tree_count += 1
        my_map = move_map(my_map, x, y)
    return tree_count


assert move_line(test_map[0], 0) == test_map[0]
assert move_line(test_map[0], 3) == "#.........#"
assert move_map_right(test_map, 0) == test_map
assert move_map_down(test_map, 1)[0] == "#...#...#.."

assert count_trees(test_map, 3, 1) == 7

print(count_trees(my_map, 3, 1))

my_num = 1
for x, y in [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)]:
    my_num *= count_trees(my_map, x, y)
print(my_num)
